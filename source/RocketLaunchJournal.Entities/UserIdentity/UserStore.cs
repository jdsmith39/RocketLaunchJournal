        protected UserClaim CreateUserClaim(User user, Claim claim)
        {
            var userClaim = new UserClaim { UserId = user.UserId };
            userClaim.InitializeFromClaim(claim);
            return userClaim;
        }

        /// <summary>
        /// Find a user token if it exists.
        /// </summary>
        /// <param name="user">The token owner.</param>
        /// <param name="loginProvider">The login provider for the token.</param>
        /// <param name="name">The name of the token.</param>
        /// <param name="cancellationToken">The <see cref="CancellationToken"/> used to propagate notifications that the operation should be canceled.</param>
        /// <returns>The user token if it exists.</returns>
        protected System.Threading.Tasks.ValueTask<UserToken> FindTokenAsync(User user, string loginProvider, string name, CancellationToken cancellationToken)
            => UserTokens.FindAsync(new object[] { user.UserId, loginProvider, name }, cancellationToken);

        /// <summary>
        /// Remove a new user token.
        /// </summary>
        /// <param name="token">The token to be removed.</param>
        /// <returns></returns>
        protected System.Threading.Tasks.Task RemoveUserTokenAsync(UserToken token)
        {
            UserTokens.Remove(token);
            return System.Threading.Tasks.Task.CompletedTask;
        }

        /// <summary>
        /// Add a new user token.
        /// </summary>
        /// <param name="token">The token to be added.</param>
        /// <returns></returns>
        protected System.Threading.Tasks.Task AddUserTokenAsync(UserToken token)
        {
            UserTokens.Add(token);
            return System.Threading.Tasks.Task.CompletedTask;
        }

        /// <summary>
        /// Called to create a new instance of a <see cref="IdentityUserToken{TKey}"/>.
